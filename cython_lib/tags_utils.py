def color_preview(key: str, value: str) -> str | None:
    """
    Return a color preview of a string.

    Returns None if the string is not a valid color.

    >>> color_preview('colour', '#ff0000')
    '#ff0000'

    >>> color_preview('colour', 'aliceblue')
    'aliceblue'

    >>> color_preview('colour', 'invalid')
    None
    """

    # small optimization, skip empty values
    if not value:
        return None

    # match keys like r':?colour$' without regex/backtracking
    if not key.endswith('colour'):
        return None
    if len(key) > 6 and key[-7] != ':':
        return None

    # match hex colors efficiently
    if value[0] == '#':
        if len(value) not in (4, 7):
            return None
        for c in value[1:]:
            if not (('0' <= c <= '9') or ('A' <= c <= 'F') or ('a' <= c <= 'f')):
                return None

        return value

    # match W3C colors
    if value.lower() in _W3C_COLORS:
        return value

    return None


